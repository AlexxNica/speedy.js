#include <array>

template<size_t length>
double computeCheckSum(const std::array<unsigned, length>& array) {
    double result = 0.0;
    for (size_t i = 0; i < array.size() - 1; i+=2) {
        result += static_cast<double>(array[i]) * static_cast<double>(array[i + 1]);
    }

    return result;
}

template<size_t length>
void merge(std::array<unsigned, length>& array, size_t lower, size_t middle, size_t upper, std::array<unsigned, length>& sorted) {
    size_t i = lower;
    size_t j = middle;

    for (size_t k = i; k < upper; ++k) {
        if (i < middle && (j >= upper || array[i] <= array[j])) {
            sorted[k] = array[i];
            ++i;
        } else {
            // j element is smaller or i has reached the middle
            sorted[k] = array[j];
            ++j;
        }
    }
}

template <size_t length>
void splitAndMerge(std::array<unsigned, length>& array, size_t lower, size_t upper, std::array<unsigned, length>& sorted) {
    if (upper - lower < 2) {
        return;
    }

    size_t middle = (((upper - lower) / 2) | 0) + lower;

    splitAndMerge(sorted, lower, middle, array);
    splitAndMerge(sorted, middle, upper, array);

    merge(array, lower, middle, upper, sorted);
}

